package problem2;

/**
 * Enumerate types for propulsion type of a boat
 * @author Arman Engin Sucu
 */
public enum PropulsionType {
  /**
   * propulsion type to represent sail power
   */
  SAIL_POWER,

  /**
   * propulsion type to represent inboard engine
   */
  INBOARD_ENGINE,

  /**
   * propulsion type to represent outboard engine
   */
  OUTBOARD_ENGINE,

  /**
   * propulsion type to represent jet propulsion
   */
  JET_PROPULSION
}
